package pxc.bandapanda;

public class OtherUsers {
	private String name;
	private int ID;
	
	public OtherUsers(String n, int i){
		name = n;
		ID = i;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	
	
}
